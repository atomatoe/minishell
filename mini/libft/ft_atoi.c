#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int		i;
	long	g;
	int		minus;
	char	*str;

	i = 0;
	g = 0;
	minus = 1;
	str = (char *)nptr;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\f'
		|| str[i] == '\t' || str[i] == '\v' || str[i] == '\r')
		i++;
	if (str[i] == '-')
	{
		minus = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		g = g * 10 + (str[i] - '0');
		i++;
	}
	return (g * minus);
}
